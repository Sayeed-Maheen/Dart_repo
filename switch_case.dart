void main() {
  var A = 80;
  switch (A) {
    case 80:
      {
        print("Excellent");
      }
      break;

    case 50:
      {
        print("Good");
      }
      break;

    case 30:
      {
        print("Fair");
      }
      break;

    default:
      {
        print("Fail");
      }
      break;
  }
}
